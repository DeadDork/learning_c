////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
int main( int argc, char * argv[] )
{
	int e;

	printf( "argc = [%d]\n", argc );
	for( e = 0; e < argc; ++e)
	{
		printf( "argv[%d] = [%s]\n", e, argv[ e ] );
	}

	return 0;
}
