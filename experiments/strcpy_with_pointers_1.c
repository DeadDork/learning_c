#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
// Functional Prototypes

void strcpy( char *s, char *t );

////////////////////////////////////////////////////////////////////////////////
// Funcions

void strcpy( char *s, char *t )
{
	while ( *s++ = *t++ );
}

////////////////////////////////////////////////////////////////////////////////
int main() {
	char *p = "hello";
	char *r = "harro";

	printf( "strcpy:\n" );
	printf( "\tBefore:\n\t\tp = [%s]\n\t\tr = [%s]\n", p, r );
	strcpy( r, p );
	printf( "\tAfter:\n\t\tp = [%s]\n\t\tr = [%s]\n", p, r );

	return 0;
}
