// Explores `return` on a void.

// Conclusion: works!

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
// Macros

#define Print(x) printf("%s = %d\n", #x, (x))

////////////////////////////////////////////////////////////////////////////////
// Function Prototypes

// Stops counting <= 10
void stop_at_ten(int);

////////////////////////////////////////////////////////////////////////////////
// Functions

void stop_at_ten(int i) {
	Print(i);

	if (i >= 10)
		return;
	else
		stop_at_ten(++i);
}

////////////////////////////////////////////////////////////////////////////////
int main(void) {
	int i;

	while (scanf("%d", &i) != 0)
		stop_at_ten(i);

	return 0;
}
