// Explores the strtod() function that comes with <stdlib.h>.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdlib.h>
#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
int main (void) {
	char ns[] = "-.0987654321"; // Number String

	printf ("string:\n\tns = [%s]\n", ns);
	printf ("double:\n\tns = [%.10f]\n", strtod (ns, NULL));

	return 0;
}
