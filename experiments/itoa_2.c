#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
// Symbolic Constants

#define MAX_STRING 100

////////////////////////////////////////////////////////////////////////////////
// Function Prototypes

// Print n in ascii
void printd(int);

////////////////////////////////////////////////////////////////////////////////
// Functions

void printd(int number) {
	if (number < 0) {
		putchar('-');
		number = -number;
	}

	if (number / 10)
		printd(number / 10);

	putchar(number % 10 + '0');
}

////////////////////////////////////////////////////////////////////////////////
int main(void) {
	int number;

	while (scanf("%d", &number)) {
		printd(number);
		putchar('\n');
	}

	return 0;
}
