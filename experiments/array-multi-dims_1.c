////////////////////////////////////////////////////////////////////////////////
// Comments

// Explores multi-dimensional arrays.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>
#include <string.h>

////////////////////////////////////////////////////////////////////////////////
// Symbolic Constants

#define MAXLEN 10

////////////////////////////////////////////////////////////////////////////////
// Macros

#define ArrayPrint(s, i, ii) (printf("s[%d][%d] = %c\n", i, ii, (s[i][ii])))

////////////////////////////////////////////////////////////////////////////////
int main(void) {
	char s[MAXLEN][MAXLEN];
	int i, ii;

	for (i = 0; i < MAXLEN; ++i)
		for (ii = 0; ii < MAXLEN; ++ii) {
			s[i][ii] = '0' + i + ii;
			ArrayPrint(s, i, ii);
		}

	return 0;
}
