// Prints the results of a series of log expressions.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>
#include <math.h>

////////////////////////////////////////////////////////////////////////////////
int main( void )
{
	int e; // Element

	for( e = 1; e < 10; ++e )
	{
		printf( "Bit count of [%d] = [%d]\n", e, ( unsigned )( log( e ) / log( 2 ) ) );
	}

	return 0;
}
