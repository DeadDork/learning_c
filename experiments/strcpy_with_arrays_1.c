// Tests the difference between assigning a string constant to a pointer vs. an array.

// Works!

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
// Function Prototypes

void strcpy( char *s, char *t );

////////////////////////////////////////////////////////////////////////////////
// Functions

void strcpy( char *s, char *t ) {
	while ( *s++ = *t++ );
}

////////////////////////////////////////////////////////////////////////////////
int main() {
	char p[] = "hello";
	char r[] = "harro";

	printf( "p = [%s]; r = [%s]\n", p, r );
	strcpy( r, p );
	printf( "p = [%s]; r = [%s]\n", p, r );

	return 0;
}
