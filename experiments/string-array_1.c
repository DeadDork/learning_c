// Tests adding a multi-character string constant midway in a character array.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
int main( void )
{
	char s[] = "Goodbye, cruel world";

	printf( "Before:\n\t[%s]\n", s );
	s[ 9 ] = "doggy";
	printf( "After:\n\t[%s]\n", s );

	return 0;
}
