// Prints the results of the modulus operator on positive & negative numbers.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
int main( void )
{
	printf( "-3 mod 2 = %d\n", -3 % 2 );
	printf( "3 mod 2 = %d\n", 3 % 2 );

	return 0;
}
