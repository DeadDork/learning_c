// Explores whether atof() can handle negative numbers.

// Works!

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>
#include <stdlib.h>

////////////////////////////////////////////////////////////////////////////////
int main(void) {
	char s[] = "-1234.5678";

	printf("%s + %d = %f\n", s, (int)(atof(s)), atof(s) - (int)(atof(s)));

	return 0;
}
