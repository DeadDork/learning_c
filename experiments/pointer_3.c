// What happens to a parameterized pointer in a function?
// Specifically, if the pointer is autoincremented in the function, does its
// address change in main?

// No, it does not! Yay!

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>
#include <stdlib.h>
#include <string.h>

////////////////////////////////////////////////////////////////////////////////
// Function Prototypes

void ptr_diddle (char * s);

////////////////////////////////////////////////////////////////////////////////
// Functions

void ptr_diddle (char * s) {
	++s;
}

////////////////////////////////////////////////////////////////////////////////
int main (void) {
	char * s = malloc (500);

	s = strcat (s, "1234");
	printf ("Before = [%s]\n", s);
	ptr_diddle (s);
	printf ("After = [%s]\n", s);

	return 0;
}
