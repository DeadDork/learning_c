int main( void )
{
	int h_H = '\x48';
	int h_e = '\x65';
	int h_x = '\x78';

	printf( "Hex = [%c][%c][%c]\n", h_H, h_e, h_x );
	printf( "\t72 = [%d]\n", h_H );
	printf( "\t101 = [%d]\n", h_e );
	printf( "\t120 = [%d]\n", h_x );

	return 0;
}
