// Tests parameterizing string constants.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
// Function Prototypes

void pprintf( char * sc );
/* Prints a string constant passed in from main().

   `sc` = the string constant. */

////////////////////////////////////////////////////////////////////////////////
// Functions

void pprintf( char * sc )
{
	printf( "pprintf:\n\tString Constant = [%s]\n", sc );
}

////////////////////////////////////////////////////////////////////////////////
int main()
{
	char * sc = "hello\0";

	printf( "printf:\n\tString Constant = [%s]\n", sc );
	pprintf( "goodbye\0" );

	return 0;
}
