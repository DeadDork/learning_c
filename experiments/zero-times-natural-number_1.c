// Prints product of 0 and a series of natural numbers.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
int main( void )
{
	int e; // Element

	for( e = 0; e <= 10; ++e )
	{
		printf( "0 x %d = %d\n", e, 0 * e );
	}

	return 0;
}
