// Plays with floating-point prints.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
int main()
{
	int x = 12;
	float y = 1.07;
	float z = 1.0;

	printf( "x = [%d]\n", x );
	printf( "y = [%4.4f]\n", y );
	printf( "z = [%4.4f]\n", z );

	return 0;
}
