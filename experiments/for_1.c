// Experiments with a complex for loop.

// Prints the number of 'on' bits.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
int main( void )
{
	int e; // Element
	unsigned x = 15;

	for( e = 1; x > 0; ( x &= x - 1 ) && ++e );
	printf( "x has %d 'on' bits\n", e - 1 );

	return 0;
}
