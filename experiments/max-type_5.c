// Looks at how many bits there are in a maxtype based on it's log.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>
#include <math.h>
#include <limits.h>

////////////////////////////////////////////////////////////////////////////////
int main( void )
{
	unsigned x = log( UINT_MAX ) / log( 2 );

	printf( "unsigned int has [%u] bits\n", x );

	return 0;
}
