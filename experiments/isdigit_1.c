// Determines whether '\0' is a digit, according to isdigit().

// It's not.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <ctype.h>
#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
int main (void) {
	int i = '\0';

	if (isdigit(i))
		printf ("%d *is* a digit\n", i);
	else
		printf ("%d is *not* a digit\n", i);

	return 0;
}
