// Explores the address of automatic variables in a recursive function (is it
// the same, is it different?)

// Conclusion: automatic variables do not use the same memory block unless
// the `static` keyword is used.

// N.B. The below segfaults. Why?

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
// Macros

#define PrintAddress(x) printf("Address %s = %p\n", #x, (&(x)))

////////////////////////////////////////////////////////////////////////////////
// Function Prototypes

void count_to_20(int counter);

////////////////////////////////////////////////////////////////////////////////
// Functions

void count_to_20(int counter) {
	int i; // Changes every recursion
	//static int i; // Stays the same

	if (counter <= 20) {
		PrintAddress(i);
		count_to_20(++counter);
	}
}

////////////////////////////////////////////////////////////////////////////////
int main(void) {
	count_to_20(-10000000);

	return 0;
}
