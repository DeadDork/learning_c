////////////////////////////////////////////////////////////////////////////////
// Comments

// Tests whether passing a pointer to a float to a function, changing it's contents, works.

// Works!

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
// Function Prototypes

void xchange( float *x );

////////////////////////////////////////////////////////////////////////////////
// Functions

void xchange( float *x )
{
	*x = 1245.3;
}

////////////////////////////////////////////////////////////////////////////////
int main()
{
	float *x;
	*x = 31.2;

	printf( "x = [%2.1f]\n", *x );
	xchange( x );
	printf( "x = [%4.1f]\n", *x );

	return 0;
}
