// Comments

// Prints results of exponents.

// 2^0 = 1!

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>
#include <math.h>

////////////////////////////////////////////////////////////////////////////////
int main( void )
{
	int e; // Element

	for( e = 0; e <= 10; ++e )
	{
		printf( "2^%d = [%d]\n", e, ( int )pow( 2, e ) );
	}

	return 0;
}
