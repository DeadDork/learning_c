////////////////////////////////////////////////////////////////////////////////
// Comments

// Prints out each character in argv[] to see whether they are automatically
// chomped.

// Conclusion: they are.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
// Symbolic Constants

#define NUL '\0'

////////////////////////////////////////////////////////////////////////////////
int main( int argc, char * argv[] )
{
	int e, ee;

	for( e = 0; e < argc; ++e )
	{
		printf( "argc = [%d]\n\t", e );
		for( ee = 0; argv[ e ][ ee ] != NUL; ++ee )
		{
			printf( "[%c]", argv[ e ][ ee ] );
		}
		putchar( '\n' );
	}

	return 0;
}
