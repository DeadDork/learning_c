// Explores use of sizeof in a malloc-ed char pointer.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>
#include <stdlib.h>

////////////////////////////////////////////////////////////////////////////////
int main (void) {
	char * cptr = malloc ((size_t) (3000 * sizeof (char)));

	printf ("sizeof (cptr) = [%d]\n(Should equal 300)\n", (int) sizeof (cptr));

	free (cptr);

	return 0;
}
