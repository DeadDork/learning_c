// Tests the conversion of a variable of type CHAR to a variable of type INT.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
int main()
{
	char c = '0';
	int x = c - '0';

	printf( "c:\n\tc = [%c]\n\td = [%d]\n", c, c );
	printf( "x:\n\tc = [%c]\n\td = [%d]\n", x, x );

	return 0;
}
