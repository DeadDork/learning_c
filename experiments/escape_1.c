#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
int main( void )
{
	int c, xp = '\\'; // Character, escape

	printf( "%c%c\n", xp, xp );

	while( ( c = getchar() ) != EOF )
	{
		printf( "%c", c );
	}

	return 0;
}
