// Reads & prints input; if any of it is an alphabetical upper case, lowers it.

////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
int main( void )
{
	int c; // Character

	while( ( c = getchar() ) != EOF )
	{
		putchar( ( c >= 'A' && c <= 'Z' ) ? ( c + ( 'a' - 'A' ) ) : c );
	}

	return 0;
}
