////////////////////////////////////////////////////////////////////////////////
// Libraries

#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
// Symbolic Constants

#define COLD 0
#define HOT 300

////////////////////////////////////////////////////////////////////////////////
// Function Prototypes

float celscon( float fahr );

////////////////////////////////////////////////////////////////////////////////
// Functions

float celscon( float fahr )
{
	return ( ( 5.0 / 9.0 ) * ( fahr - 32.0 ) );
}

////////////////////////////////////////////////////////////////////////////////
int main()
{
	float f; // Fahrenheit
	int step = 20;

	/* Print Fahrenheit & Celsius conversion table */
	printf( "+------------+---------+\n" );
	printf( "| Fahrenheit | Celsius |\n" );
	printf( "+------------+---------+\n" );
	for( f = COLD; f <= HOT; f = f + step )
	{
		printf( "|%11.1f |%8.1f |\n", f, celscon( f ) );
	}
	printf( "+------------+---------+\n" );

	return 0;
}
